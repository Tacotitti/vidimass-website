# Add to translations.js
with open('translations.js', 'r', encoding='utf-8') as f:
    content = f.read()

for lang, keys in contact_translations.items():
    insertion = ''
    for key, value in keys.items():
        value_escaped = value.replace('"', '\\"')
        insertion += f'\n        {key}: "{value_escaped}",'
    
    # Find cta_contact and insert after
    pattern = r'cta_contact: "[^"]*",'
    import re
    matches = list(re.finditer(pattern, content))
    
    lang_order = ['en', 'de', 'tr', 'pt', 'es', 'ru', 'zh', 'ar']
    lang_index = lang_order.index(lang)
    
    if lang_index < len(matches):
        match = matches[lang_index]
        content = content[:match.end()] + insertion + content[match.end():]
        print(f"✓ Added {len(keys)} contact page keys to {lang}")

with open('translations.js', 'w', encoding='utf-8') as f:
    f.write(content)

print("\n✅ Complete contact page translations added to all 8 languages!")
print(f"Total keys per language: {len(contact_translations['en'])}")
